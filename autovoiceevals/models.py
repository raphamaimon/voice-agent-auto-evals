"""Typed data models for AutoVoiceEvals.

Every data structure that flows between modules is defined here.
No business logic — just shapes and conversions.
"""

from __future__ import annotations

from dataclasses import dataclass, field, asdict


# ---------------------------------------------------------------------------
# Conversation
# ---------------------------------------------------------------------------

@dataclass
class Turn:
    """A single turn in a conversation."""
    role: str          # "caller" or "assistant"
    content: str
    latency_ms: float = 0.0


@dataclass
class Conversation:
    """A multi-turn conversation between a caller and the AI agent."""
    scenario_id: str
    turns: list[Turn] = field(default_factory=list)
    total_cost: float = 0.0
    avg_latency_ms: float = 0.0
    error: str = ""

    @property
    def transcript(self) -> str:
        return "\n".join(
            f"{'CALLER' if t.role == 'caller' else 'AGENT'}: {t.content}"
            for t in self.turns
        )

    @property
    def agent_turns(self) -> list[Turn]:
        return [t for t in self.turns if t.role == "assistant"]


# ---------------------------------------------------------------------------
# Scenario
# ---------------------------------------------------------------------------

@dataclass
class Scenario:
    """An adversarial test scenario generated by the LLM."""
    id: str
    persona_name: str = ""
    persona_background: str = ""
    difficulty: str = "B"
    attack_strategy: str = ""
    voice_characteristics: dict = field(default_factory=dict)
    caller_script: list[str] = field(default_factory=list)
    agent_should: list[str] = field(default_factory=list)
    agent_should_not: list[str] = field(default_factory=list)

    @classmethod
    def from_dict(cls, data: dict) -> Scenario:
        return cls(
            id=data.get("id", "unknown"),
            persona_name=data.get("persona_name", ""),
            persona_background=data.get("persona_background", ""),
            difficulty=data.get("difficulty", "B"),
            attack_strategy=data.get("attack_strategy", ""),
            voice_characteristics=data.get("voice_characteristics", {}),
            caller_script=data.get("caller_script", []),
            agent_should=data.get("agent_should", []),
            agent_should_not=data.get("agent_should_not", []),
        )

    def to_dict(self) -> dict:
        return asdict(self)


# ---------------------------------------------------------------------------
# Dataset item (single-turn evaluation)
# ---------------------------------------------------------------------------

@dataclass
class DatasetItem:
    """A single evaluation item: frozen conversation context + criteria.

    Used for single-turn evaluation where the model produces ONE response
    given the conversation history.
    """
    id: str
    conversation_context: list[dict] = field(default_factory=list)  # [{"role": "user"|"assistant", "content": "..."}]
    agent_should: list[str] = field(default_factory=list)
    agent_should_not: list[str] = field(default_factory=list)
    scenario_type: str = "middle"     # "beginning" | "middle" | "end"
    description: str = ""
    difficulty: str = "B"             # A=easy, B=medium, C=hard, D=adversarial
    category: str = "routine"         # "routine" | "business" | "edge_case" | "adversarial" | "voice_specific" | "known_issues" | "brevity_test"
    voice_context: dict = field(default_factory=dict)   # {"accent": "...", "noise": "...", "pace": "..."}
    severity: str = "medium"           # "critical" | "high" | "medium" | "low"
    weight: float = 1.0                # scenario weight for aggregate scoring

    @classmethod
    def from_dict(cls, data: dict) -> DatasetItem:
        return cls(
            id=data.get("id", "unknown"),
            conversation_context=data.get("conversation_context", []),
            agent_should=data.get("agent_should", []),
            agent_should_not=data.get("agent_should_not", []),
            scenario_type=data.get("scenario_type", "middle"),
            description=data.get("description", ""),
            difficulty=data.get("difficulty", "B"),
            category=data.get("category", "routine"),
            voice_context=data.get("voice_context", {}),
            severity=data.get("severity", "medium"),
            weight=data.get("weight", 1.0),
        )

    def to_dict(self) -> dict:
        return asdict(self)

    @property
    def context_transcript(self) -> str:
        """Human-readable transcript of the conversation context."""
        return "\n".join(
            f"{'CALLER' if m['role'] == 'user' else 'AGENT'}: {m['content']}"
            for m in self.conversation_context
        )


# ---------------------------------------------------------------------------
# Evaluation
# ---------------------------------------------------------------------------

@dataclass
class EvalResult:
    """Result of evaluating a single scenario against the agent."""
    scenario_id: str
    persona: str
    score: float
    csat_score: int
    passed: bool
    should_score: float = 0.0
    should_not_score: float = 0.0
    failure_modes: list[str] = field(default_factory=list)
    issues: list[dict] = field(default_factory=list)
    summary: str = ""
    strengths: list[str] = field(default_factory=list)
    weaknesses: list[str] = field(default_factory=list)
    transcript: str = ""
    agent_response: str = ""         # single-turn mode
    num_turns: int = 0
    avg_latency_ms: float = 0.0
    voice_quality: dict = field(default_factory=dict)   # {brevity: {score, word_count}, naturalness: {...}, ...}
    quality_score: float = 0.0       # normalized 0-1 from voice_quality dimensions
    weight: float = 1.0             # propagated from DatasetItem

    @classmethod
    def from_dict(cls, data: dict) -> EvalResult:
        return cls(
            scenario_id=data.get("scenario_id", ""),
            persona=data.get("persona", ""),
            score=data.get("score", 0.0),
            csat_score=data.get("csat_score", 50),
            passed=data.get("passed", False),
            should_score=data.get("should_score", 0.0),
            should_not_score=data.get("should_not_score", 0.0),
            failure_modes=data.get("failure_modes", []),
            issues=data.get("issues", []),
            summary=data.get("summary", ""),
            strengths=data.get("strengths", []),
            weaknesses=data.get("weaknesses", []),
            transcript=data.get("transcript", ""),
            agent_response=data.get("agent_response", ""),
            num_turns=data.get("num_turns", 0),
            avg_latency_ms=data.get("avg_latency_ms", 0.0),
            voice_quality=data.get("voice_quality", {}),
            quality_score=data.get("quality_score", 0.0),
            weight=data.get("weight", 1.0),
        )

    def to_dict(self) -> dict:
        return asdict(self)


@dataclass
class Metrics:
    """Aggregated metrics from a set of eval results."""
    avg_score: float
    avg_csat: float
    pass_rate: float
    n_passed: int
    n_total: int
    unique_failures: list[str] = field(default_factory=list)


# ---------------------------------------------------------------------------
# Experiment history
# ---------------------------------------------------------------------------

@dataclass
class ExperimentRecord:
    """Lightweight record of an experiment for history tracking."""
    number: int
    score: float
    status: str           # "keep", "discard", "skip"
    description: str
    prompt_len: int = 0
    change_type: str = ""  # "add" | "modify" | "remove" | "reorder"
